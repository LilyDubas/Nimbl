          <p>Le phoque fait partie de la famille des <b>phocidés</b> (la même famille que l’éléphant de mer). Il mesure environ entre 1m60 et 1m80 et pèse de 80 à 170 kg! Sa graisse le protège de l’eau froide et il aime se prélasser au soleil sur les bancs de sables. On reconnaît bien sa forme sur les bancs de sables, en arc ou en banane avec la queue et le museau en l’air.</p>
