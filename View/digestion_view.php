            <p class="text-muted">Qu’arrive-t-il à la nourriture une fois que tu l’as mangé?</p>
              <p>L’appareil digestif de l’être humain est un système assez complexe qui contient plusieurs étapes.</p>
              <p>Les différents organes qui le composent sont : la bouche, l’oesophage, l’estomac, l’intestin grêle, le gros intestin, le rectum et l’anus.</p>
              <p>Lorsque tu manges un aliment, comme une pomme par exemple, tu le mets d’abord dans ta bouche. <strong class="text-danger">C’est là que la digestion commence!</strong></p>
              <p>Tes dents vont couper et broyer les aliments, et la salive va aider à transformer le tout en une sorte de boule compacte et homogène qui s’appelle le <strong>“bol alimentaire”.</strong></br>
              La langue a aussi un rôle important, elle apporte le goût de l’aliment (que tu ne mangeras finalement pas si tu ne le trouves pas à ton goût!), mélange les aliments avec la salive et pousse les aliments vers l’arrière afin que tu les avale. Cela s’appelle <strong>la déglutition.</strong></p>
              <p>Une fois avalée, la nourriture arrive dans <strong class="text-danger">l’oesophage.</strong> C’est comme un grand tube d’environ 25 à 30cm de long. C’est l’oesophage qui fait le pont entre la bouche et l’estomac.</p>
              <p><strong class="text-danger">L’estomac</strong> quant à lui est une grosse poche, un peu en forme de J et qui peut contenir jusqu’à 4 litres de liquides ou d’aliments. Sa principale mission est de transformer les aliments en bouillie grâce à l’acide qu’il produit. Cette bouillie créée par l’estomac s’appelle “chyme”.</p>
              <p>Une fois transformés en bouillie dans l’estomac, les aliments poursuivent leur chemin vers <strong class="text-danger">l’intestin grêle.</strong> Celui-ci est comme un tube replié en zig-zag et qui mesure 4 à 7m de long (pour comparer la taille, 4 m représente la longueur d’une voiture et 7 m représente la taille d’un minibus). Durant son voyage dans l’intestin grêle, le chyme va presque complètement disparaître.</p>
              <p>Les restes de nourriture qui ont résisté à l’action de l’estomac et de l’intestin grêle continuent leur chemin et arrivent au <strong class="text-danger">gros intestin</strong> ou <strong class="text-danger">côlon.</strong> Ce tube est moins long que l’intestin grêle et mesure entre 80 et 150 cm en moyenne. Il transforme la bouillie liquide en une masse solide appelée “les selles” ou plus communément chez les enfants le “caca”. </p>
              <p>Le côlon se termine par le <strong class="text-danger">rectum</strong> qui mesure environ 15cm de long et se prolonge par <strong class="text-danger">l’anus</strong> d’où sont expulsées les selles.</p>
              <h5 class="text-success">Le savais-tu?</h5>
